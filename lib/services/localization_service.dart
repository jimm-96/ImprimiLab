  static String translate(String lang, String key) {
    final language = _localizedValues.containsKey(lang) ? lang : 'es';
    return _localizedValues[language]?[key] ?? key;
  }

  static String formatCurrency(double amount, String currencyCode) {
    String locale = 'es_CL';
    String symbol = '\$';

    switch (currencyCode) {
      case 'CLP':
        locale = 'es_CL';
        symbol = '\$';
        break;
      case 'USD':
        locale = 'en_US';
        symbol = '\$';
        break;
      case 'EUR':
        locale = 'de_DE';
        symbol = '€';
        break;
      case 'MXN':
        locale = 'es_MX';
        symbol = '\$';
        break;
      case 'ARS':
        locale = 'es_AR';
        symbol = '\$';
        break;
      default:
        locale = 'en_US';
        symbol = currencyCode;
    }

    final formatter = NumberFormat.currency(
      locale: locale,
      symbol: symbol,
      decimalDigits: (currencyCode == 'CLP') ? 0 : 2,
    );
    return formatter.format(amount);
  }
